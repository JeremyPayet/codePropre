package ex3;

/**
 * L'énumération des comportements (régimes alimentaires) d'un animal
 */
public enum Comportement {
	/**
	 * L'animal ne mange que des animaux
	 */
	CARNIVORE,
	/**
	 * L'animal ne mange que des végétaux
	 */
	HERBIVORE,
	/**
	 * L'animal peut manger des animaux ou des végétaux
	 */
	OMNIVORE
}
